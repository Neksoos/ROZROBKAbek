# data/gathering_stories.py
"""
Сценарії походів за ресурсами.

Ідея:
- Для кожної зони є набір "малих пригод" (story events).
- Кожна пригода прив’язана до зони, базового ризику і до конкретних мобів.
- Фронт/бек можуть рандомно брати одну пригоду по зоні + risk_mode
  і показувати гравцю варіанти дій.

Рівні ризику (risk_mode):
- "careful"  – обережний збір (менше луту, менше небезпеки)
- "normal"   – звичайний збір
- "risky"    – ризикований, більше шансів на засідку/рідкісний лут
"""

from __future__ import annotations

from typing import Literal, TypedDict, List, Dict, Optional

RiskMode = Literal["careful", "normal", "risky"]


class StoryOption(TypedDict):
    code: str                 # код дії, що піде в API/фронт (наприклад "avoid", "fight"…)
    label: str                # текст кнопки
    consequence: str          # короткий код наслідку: "nothing", "small_loot", "big_loot", "battle", "trap", …
    description: str          # опис того, що відбувається, коли гравець обирає цю дію


class StoryTemplate(TypedDict):
    id: str                   # унікальний id сценарію
    area_key: str             # ключ зони (slums, swamp, …)
    risk_mode: RiskMode       # базовий ризик, під який задумана ця сцена
    title: str                # короткий заголовок
    intro: str                # вступний опис ситуації
    mob_names: List[str]      # які моби теоретично можуть тут з’явитися
    options: List[StoryOption]


